package system.properties;

public class SystemProperty
{
    public String bodhi;

    public String key;

    public Object value;

    public SystemProperty(String bodhi, Object value)
    {
        this.bodhi = bodhi;

        this.key = key;

        this.value = value;
    }

    public SystemProperty(String bodhi, String key, Object value)
    {
        this.bodhi = bodhi;

        this.key = key;

        this.value = value;
    }


}
